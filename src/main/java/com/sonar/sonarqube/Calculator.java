package com.sonar.sonarqube;

public class Calculator {

    public int sum(int a, int b) {
        return a+b;
    }

    public int subtract(int a, int b) {
        return a-b;
    }

    public int multiply(int a, int b) {
        return a*b;
    }

    public float divide(float a, float b) {
        return a/b;
    }

}
